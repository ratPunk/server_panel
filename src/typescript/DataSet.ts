// Начальные данные
export const dataset = [
    { x: 1, y: 2 },
    { x: 2, y: 5.5 },
    { x: 3, y: 2 },
    { x: 4, y: 8.5 },
    { x: 5, y: 1.5 },
    { x: 6, y: 14 },
    { x: 7, y: 2 },
    { x: 8, y: 5.5 },
    { x: 9, y: 2 },
    { x: 10, y: 8.5 },
    { x: 11, y: 1.5 },
    { x: 12, y: 14 },
];

// Функция для обновления данных
export function updateDataset() {
    // Удаляем первую точку
    dataset.shift();

    // Находим последнее значение x
    const lastX = dataset[dataset.length - 1]?.x || 12;

    // Генерируем новое случайное значение y (например, от 0 до 20)
    const newY = Math.round((Math.random() * 20 * 100)) / 100; // 2 знака после запятой

    // Добавляем новую точку в конец
    dataset.push({ x: lastX + 1, y: newY });

    // Возвращаем копию массива, чтобы триггерить обновление в React (если нужно)
    return [...dataset];
}